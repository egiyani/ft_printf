#include "./get_next_line.h"

int	readfile(int fd, char **buf)
{
	int i;

	i = 0;
	if (!fd || !buf || fd < 0)
		return (-1);
	while (get_next_line(fd, &buf[i]) != 0)
		++i;
	return (0);
}
